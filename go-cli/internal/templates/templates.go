// ReadinessPresets enumerates the supported `init --preset` values.
var ReadinessPresets = []string{"nextjs", "laravel", "symfony", "node-api", "docker-swarm"}

// IsReadinessPreset reports whether name is a supported preset.
func IsReadinessPreset(name string) bool {
	for _, p := range ReadinessPresets {
		if p == name {
			return true
		}
	}
	return false
}

// DeployShuttleYML returns an opinionated `.shuttle.yml` for the given
// preset. domain may be empty; it is rendered as a placeholder users can edit.
func DeployShuttleYML(preset string, domain string) string {
	if domain == "" {
		domain = "app.example.com"
	}
	switch preset {
	case "nextjs":
		return fmt.Sprintf(`version: 1

# Generated by 'shuttle init --preset nextjs'.
# Adjust app.domain and ignore lists as the project evolves.

app:
  domain: %s
  healthcheckPath: /api/health

checks:
  profile: [docker, caddy]
  ignore:
    # Adminer is rare in a Next.js stack; remove this line if you ship one.
    - adminer.ip_restriction_missing

docker:
  # No long-running queue workers in a typical Next.js setup.
  workerServices: []
`, domain)

	case "laravel":
		return fmt.Sprintf(`version: 1

# Generated by 'shuttle init --preset laravel'.
# Tune the queue / horizon / scheduler patterns to your container naming.

app:
  domain: %s
  healthcheckPath: /up

checks:
  profile: [docker, caddy]

docker:
  # Laravel queue workers, Horizon supervisors, and schedulers do not expose
  # an HTTP healthcheck; treat them as workers so doctor stops flagging them.
  workerServices:
    - "*-queue"
    - "*-horizon"
    - "*-scheduler"
    - "*-worker"
  # Octane / RoadRunner images sometimes require root; allow-list as needed.
  allowRoot: []
  # Dozzle / log shippers that legitimately mount the Docker socket.
  allowDockerSocket:
    - dozzle*
`, domain)

	case "symfony":
		return fmt.Sprintf(`version: 1

# Generated by 'shuttle init --preset symfony'.
# Adjust domain and messenger worker patterns as needed.

app:
  domain: %s
  healthcheckPath: /

checks:
  profile: [docker, caddy]

docker:
  # Symfony Messenger consumers do not expose HTTP healthchecks; list them
  # as workers so doctor stops flagging missing healthchecks.
  workerServices:
    - "*-messenger"
    - "*-consumer"
    - "*-worker"
  allowRoot: []
  allowDockerSocket:
    - dozzle*
`, domain)

	case "node-api":
		return fmt.Sprintf(`version: 1

# Generated by 'shuttle init --preset node-api'.

app:
  domain: %s
  healthcheckPath: /health

checks:
  profile: [docker, caddy]
  ignore:
    - adminer.ip_restriction_missing

docker:
  workerServices: []
`, domain)

	case "docker-swarm":
		return fmt.Sprintf(`version: 1

# Generated by 'shuttle init --preset docker-swarm'.
# Worker patterns assume the common '<stack>_<role>' Swarm naming.

app:
  domain: %s
  healthcheckPath: /health

checks:
  profile: [docker, caddy]

docker:
  workerServices:
    - "*_worker"
    - "*_consumer"
    - "*_scheduler"
    - "*_cron"
  allowDockerSocket:
    - dozzle*
    - portainer_agent*
`, domain)
	}
	return ""
}

func ShuttleYML(app string, domain string, host string, user string, port int) string {
	if port == 0 {
		port = 22
	}
	return fmt.Sprintf(`app: %s
domain: %s
server:
  host: %s
  user: %s
  port: %d

deploy:
  strategy: swarm

services:
  web:
    port: 3000
`, app, domain, host, user, port)
}

func Caddyfile(cfg *config.Config, upstream string) string {
	domains := Domains(cfg)
	var b strings.Builder
	for _, domain := range domains {
		fmt.Fprintf(&b, "%s {\n", domain)
		if len(cfg.Proxy.Headers) > 0 {
			b.WriteString("  header {\n")
			for key, value := range cfg.Proxy.Headers {
				fmt.Fprintf(&b, "    %s %s\n", key, value)
			}
			b.WriteString("  }\n")
		}
		fmt.Fprintf(&b, "  reverse_proxy %s\n", upstream)
		b.WriteString("}\n\n")
	}
	return b.String()
}

func ComposeDev(cfg *config.Config) string {
	var b strings.Builder
	b.WriteString("services:\n")
	for name, service := range cfg.Services {
		fmt.Fprintf(&b, "  %s:\n", name)
		b.WriteString("    build: .\n")
		if service.Command != "" {
			fmt.Fprintf(&b, "    command: %q\n", service.Command)
		}
		if service.Port != 0 {
			fmt.Fprintf(&b, "    ports:\n      - %q\n", fmt.Sprintf("%d:%d", service.Port, service.Port))
		}
	}
	return b.String()
}

// SwarmStackTemplate returns a Swarm stack YAML template for a given app.
// This is used by the init command to generate a sample docker-stack.yml.
// The registryAddr is the ephemeral registry address (e.g. "127.0.0.1:5080").
func SwarmStackTemplate(app, port, registryAddr string) string {
	if port == "" {
		port = "3000"
	}
	return fmt.Sprintf(`version: "3.8"
services:
  web:
    image: %s/%s-web:latest
    env_file: .env
    networks:
      - caddy_network
      - default
    deploy:
      replicas: 1
      update_config:
        parallelism: 1
        delay: 5s
        order: start-first
        failure_action: rollback
      rollback_config:
        parallelism: 1
        delay: 5s
        order: stop-first
      restart_policy:
        condition: on-failure
        delay: 5s
        max_attempts: 3
      labels:
        shuttle.app: %s
    healthcheck:
      test: ["CMD", "php", "-r", "exit(false === @file_get_contents('http://127.0.0.1:%s/', context: stream_context_create(['http' => ['timeout' => 3]])) ? 1 : 0);"]
      interval: 10s
      timeout: 5s
      start_period: 30s
      retries: 3

networks:
  caddy_network:
    external: true
  default:
    name: %s_network
`, registryAddr, app, app, port, app)
}

func Domains(cfg *config.Config) []string {
	switch v := cfg.Domain.(type) {
	case string:
		return []string{v}
	case []any:
		out := []string{}
		for _, item := range v {
			if s, ok := item.(string); ok {
				out = append(out, s)
			}
		}
		return out
	case []string:
		return v
	default:
		return []string{}
	}
}
